

package com.hommy.entity;


public class Wards {
    
    private int wards_id;
    private int district_district_id;
    private String wards_name;

    public Wards() {
    }

    public int getWards_id() {
        return wards_id;
    }

    public void setWards_id(int wards_id) {
        this.wards_id = wards_id;
    }

    public int getDistrict_district_id() {
        return district_district_id;
    }

    public void setDistrict_district_id(int district_district_id) {
        this.district_district_id = district_district_id;
    }

    public String getWards_name() {
        return wards_name;
    }

    public void setWards_name(String wards_name) {
        this.wards_name = wards_name;
    }

}
